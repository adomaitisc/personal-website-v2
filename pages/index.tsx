import type { NextPage } from "next";
import Head from "next/head";
import { useRef, useState } from "react";
import About from "../components/About";
import Contact from "../components/Contact";
import Hello from "../components/Hello";
import Navbar from "../components/Navbar";
import Project from "../components/Project";

const Home: NextPage = () => {
  const [currentPage, setCurrentPage] = useState<HTMLElement>();

  const refs = {
    hello: useRef<HTMLDivElement>(null),
    projects: useRef<HTMLDivElement>(null),
    about: useRef<HTMLDivElement>(null),
    contact: useRef<HTMLDivElement>(null),
  };

  const handleSelect = (e: any) => {
    e.preventDefault();
    let element = e.target;
    if (element.id === "hello") {
      refs.hello.current!.scrollIntoView({ behavior: "smooth" });
    }
    if (element.id === "projects") {
      refs.projects.current!.scrollIntoView({ behavior: "smooth" });
    }
    if (element.id === "about") {
      refs.about.current!.scrollIntoView({ behavior: "smooth" });
    }
    if (element.id === "contact") {
      refs.contact.current!.scrollIntoView({ behavior: "smooth" });
    }
  };

  return (
    <main className="w-screen h-full min-h-full box-border overflow-x-hidden overflow-y-hidden snap snap-y snap-mandatory bg-black/50">
      <Head>
        <title>Personal Website V4</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="flex flex-row w-full">
        <div className="w-full h-screen items-start overflow-y-scroll snap snap-y snap-mandatory">
          <div className="w-1/2 items-end">
            <Hello ref={refs.hello} />
            <Project ref={refs.projects} />
            <About ref={refs.about} />
            <Contact ref={refs.contact} />
          </div>
        </div>

        <div className="pointer-events-none flex flex-col w-1/2 fixed right-0 top-0 bottom-0">
          <Navbar onClick={handleSelect} />
        </div>
      </div>
    </main>
  );
};

export default Home;
